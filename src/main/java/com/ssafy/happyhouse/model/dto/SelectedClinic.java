package com.ssafy.happyhouse.model.dto;

public class SelectedClinic {
	private int no;
	private String date;
	private String city;
	private String gugun;
	private String name;
	private String address;
	private String timeWeekday;
	private String timeSat;
	private String timeHoliday;
	private String phone;

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getGugun() {
		return gugun;
	}

	public void setGugun(String gugun) {
		this.gugun = gugun;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getTimeWeekday() {
		return timeWeekday;
	}

	public void setTimeWeekday(String timeWeekday) {
		this.timeWeekday = timeWeekday;
	}

	public String getTimeSat() {
		return timeSat;
	}

	public void setTimeSat(String timeSat) {
		this.timeSat = timeSat;
	}

	public String getTimeHoliday() {
		return timeHoliday;
	}

	public void setTimeHoliday(String timeHoliday) {
		this.timeHoliday = timeHoliday;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
